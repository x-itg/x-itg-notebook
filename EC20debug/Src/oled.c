void OLED_Fill(unsigned char fill_Data)
{

  unsigned char m, n;
  for (m = 0; m < 8; m++)
  {
    WriteCmd(0xb0 + m);
    WriteCmd(0x00);
    WriteCmd(0x10);
    for (n = 0; n < 128; n++)
    {
      WriteDat(fill_Data);
    }
  }
}
//--------------------------------------------------------------
void OLED_CLS(void)
{

  OLED_Fill(0x00);
}

