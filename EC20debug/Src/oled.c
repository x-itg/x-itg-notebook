void OLED_Set_Pos(unsigned char x, unsigned char y)
{

  WriteCmd(0xb0 + y);
  delay_us(DELAYUS);
  WriteCmd(((x & 0xf0) >> 4) | 0x10);
  delay_us(DELAYUS);
  WriteCmd((x & 0x0f));
  delay_us(DELAYUS);
}

